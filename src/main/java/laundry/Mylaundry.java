    private void btn_transaksiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_transaksiActionPerformed
        // TODO add your handling code here:
        String url =
        "INSERT INTO data_laundry " +
        "(jenis_cuci,harga,berat,total_harga,bayar,kembalian) " +
        "VALUES (?,?,?,?,?,?)";

if (combo_jenis.getSelectedItem().equals("Pilih Jenis")
        || txt_harga.getText().isEmpty()
        || txt_berat.getText().isEmpty()
        || txt_ttlharga.getText().isEmpty()
        || txt_bayar.getText().isEmpty()) {

    JOptionPane.showMessageDialog(null,
            "Isi terlebih dahulu semua kolom");

} else {    

    int bayar = Integer.parseInt(txt_bayar.getText());
    int totalHarga = Integer.parseInt(txt_ttlharga.getText());

    int kembalian = bayar - totalHarga;

    try {

        PreparedStatement stat = conn.prepareStatement(url);

        stat.setString(1,
                String.valueOf(combo_jenis.getSelectedItem()));

        stat.setString(2, txt_harga.getText());
        stat.setString(3, txt_berat.getText());
        stat.setString(4, txt_ttlharga.getText());
        stat.setString(5, txt_bayar.getText());
        stat.setString(6, String.valueOf(kembalian));
        
        stat.executeUpdate();

        JOptionPane.showMessageDialog(null,
                "Kembalian Anda :\n" + kembalian);

    } catch (SQLException e) {

        JOptionPane.showMessageDialog(null,
                "Transaksi Gagal" + e);

    }   

    reset();
    Tampilkan_Data();
}
    }//GEN-LAST:event_btn_transaksiActionPerformed

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:
    if (id_transaksi.equals("")) {

        JOptionPane.showMessageDialog(
                null,
                "Pilih data terlebih dahulu");

    } else {

        try {

            int bayar =
                    Integer.parseInt(txt_bayar.getText());

            int totalHarga =
                    Integer.parseInt(txt_ttlharga.getText());

            int kembalian =
                    bayar - totalHarga;

            String sql =
                    "UPDATE data_laundry SET "
                    + "jenis_cuci=?, "
                    + "harga=?, "
                    + "berat=?, "
                    + "total_harga=?, "
                    + "bayar=?, "
                    + "kembalian=? "
                    + "WHERE field_id=?";

            PreparedStatement stat =
                    conn.prepareStatement(sql);

            stat.setString(1,
                    combo_jenis.getSelectedItem().toString());

            stat.setString(2,
                    txt_harga.getText());

            stat.setString(3,
                    txt_berat.getText());

            stat.setString(4,
                    txt_ttlharga.getText());

            stat.setString(5,
                    txt_bayar.getText());

            stat.setString(6,
                    String.valueOf(kembalian));

            stat.setString(7,
                    id_transaksi);

            stat.executeUpdate();

            JOptionPane.showMessageDialog(
                    null,
                    "Data berhasil diupdate");

            reset();
            Tampilkan_Data();

        } catch (Exception e) {

            JOptionPane.showMessageDialog(
                    null,
                    "Update gagal : "
                    + e.getMessage());

        }
    }
        
    }//GEN-LAST:event_btn_updateActionPerformed

