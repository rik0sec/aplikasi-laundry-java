    public void Tampilkan_Data() {

    String[] Data = {
    "ID Transaksi",
    "Jenis Cuci",
    "Harga/Kg",
    "Berat(Kg)",
    "Total Harga",
    "Bayar",
    "Kembalian"
};

    tabmodel = new DefaultTableModel(null, Data);
    tabel_data.setModel(tabmodel);

    String sql = "select * from data_laundry";

    try {

        Statement stat = conn.createStatement();
        ResultSet hasil = stat.executeQuery(sql);

        while (hasil.next()) {

            String FieldId = String.valueOf(
                    hasil.getInt("field_id"));

            String JenisCuci =
                    hasil.getString("jenis_cuci");

            String Harga =
                    hasil.getString("harga");

            String Berat =
                    hasil.getString("berat");

            String TotalHarga =
                    hasil.getString("total_harga");

            String Bayar =
                    hasil.getString("bayar");
            
            String Kembalian =
                    hasil.getString("kembalian");

            String[] DataLaundry = {
                    FieldId,
                    JenisCuci,
                    Harga,
                    Berat,
                    TotalHarga,
                    Bayar,
                    Kembalian
            };

            tabmodel.addRow(DataLaundry);
        }

    } catch (SQLException ex) {

        Logger.getLogger(Mylaundry.class.getName())
        .log(Level.SEVERE, null, ex);
    }
}

